import { createRequire } from 'node:module';
import fs from 'node:fs';
import path from 'node:path';

const require = createRequire(import.meta.url);
const katex = require('katex');

const dist = path.resolve('node_modules/katex/dist');
const fontsDir = path.join(dist, 'fonts');

// Inline each font as a woff2 data URI (drop woff/ttf fallbacks) so KaTeX renders
// fully offline with no network and no external font files.
let css = fs.readFileSync(path.join(dist, 'katex.min.css'), 'utf8');
css = css.replace(
  /url\(fonts\/([\w.-]+\.woff2)\)\s*format\("woff2"\)(?:\s*,\s*url\(fonts\/[\w.-]+\)\s*format\("[^"]+"\))*/g,
  (_match, file) => {
    const base64 = fs.readFileSync(path.join(fontsDir, file)).toString('base64');
    return `url(data:font/woff2;base64,${base64}) format("woff2")`;
  }
);

const km = (latex) => katex.renderToString(latex, { throwOnError: false, displayMode: false });
const esc = (s) => s.replace(/&/g, '&amp;').replace(/</g, '&lt;').replace(/>/g, '&gt;');

// Formula blocks: the equation and its "where" list. Symbols are typeset with
// KaTeX; the descriptions stay as plain (native-looking) text.
const BLOCKS = {
  rms: {
    equation: String.raw`V_{\text{rms}} = \dfrac{V_{\text{peak}}}{\sqrt{2}}`,
    where: [
      { sym: String.raw`V_{\text{rms}}`, desc: 'effective RMS voltage' },
      { sym: String.raw`V_{\text{peak}}`, desc: 'peak voltage' },
    ],
  },
  power: {
    equation: String.raw`P = V \cdot I \cdot \cos\varphi`,
    where: [
      { sym: 'P', desc: 'real power (W)' },
      { sym: 'V', desc: 'RMS voltage (V)' },
      { sym: 'I', desc: 'RMS current (A)' },
      { sym: String.raw`\cos\varphi`, desc: 'power factor' },
    ],
  },
};

// Standalone unit labels shown next to reading titles.
const UNITS = {
  unitV: String.raw`(\mathrm{V})`,
  unitA: String.raw`(\mathrm{A})`,
  unitW: String.raw`(\mathrm{W\,/\,kW})`,
  unitHz: String.raw`(\mathrm{Hz})`,
};

const rendered = {};
for (const [key, block] of Object.entries(BLOCKS)) {
  const defs = block.where
    .map((w) => `<div class="def"><span class="sym">${km(w.sym)}</span><span class="desc">${esc(w.desc)}</span></div>`)
    .join('');
  rendered[key] = `<div class="eq">${km(block.equation)}</div><div class="wh">where:</div><div class="defs">${defs}</div>`;
}
for (const [key, latex] of Object.entries(UNITS)) {
  rendered[key] = km(latex);
}

const keys = Object.keys(rendered);
const union = keys.map((k) => JSON.stringify(k)).join(' | ');
const entries = keys.map((k) => `  ${JSON.stringify(k)}: ${JSON.stringify(rendered[k])},`).join('\n');

const out =
  '// Generated by scripts/build-katex-assets.mjs. Do not edit by hand.\n' +
  '// Regenerate with: node scripts/build-katex-assets.mjs\n' +
  `export const KATEX_CSS = ${JSON.stringify(css)};\n\n` +
  `export type FormulaName = ${union};\n\n` +
  `export const RENDERED: Record<FormulaName, string> = {\n${entries}\n};\n`;

fs.writeFileSync(path.resolve('src/lib/katex-assets.ts'), out);
console.log(`Wrote src/lib/katex-assets.ts (${Math.round(out.length / 1024)} KB), ${keys.length} formulas`);
